//Object.entries

const obj = { a: 1, b: 2, c: 3 };
console.log(Object.entries(obj)); 
// [ ['a', 1], ['b', 2], ['c', 3] ]

//----------------------------------------------------

//Object.values

const obj1 = { a: 1, b: 2, c: 3 };
console.log(Object.values(obj1)); 
// [1, 2, 3]

//-----------------------------------------------------

// String padding 

console.log('5'.padStart(3, '0')); // '005'
console.log('5'.padEnd(3, '0'));   // '500'

